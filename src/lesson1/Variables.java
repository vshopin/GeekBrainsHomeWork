package lesson1;

public class Variables {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a);

        long b = 1_000_000L;
        System.out.println(b);

        float c = 3.5f;
        System.out.println(c);

        double d = 1.5;
        System.out.println(d);

        char e = 'i';
        System.out.println(e);

        boolean f = true;
        System.out.println(f);

        String str = "Hello";
        System.out.println(str);
    }
}
